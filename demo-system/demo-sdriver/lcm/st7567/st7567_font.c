/**************************************************************************************
* FunctionName   : st7567_font_utf8_get_dot()
* Description    : ST7567,获取UTF8字符的字库点阵地址
* EntryParameter : utf8,UTF8字符索引
* ReturnValue    : 返回点阵数据指针,NULL(失败)
**************************************************************************************/
const uint8_t *st7567_font_utf8_get_dot(uint8_t *utf8)
{
    uint16_t i = 0;

    for(i = 0; i < ARRAY_LEN(utf8_font); i++){
        if(utf8[0] < 0xA1 && 0 == memcmp(utf8_font[i].index, utf8, 1)){
            return utf8_font[i].dot;
        }else if(0 == memcmp(utf8_font[i].index, utf8, 3)){
            return utf8_font[i].dot;
        }
    }

    return NULL;
}

/**************************************************************************************
* FunctionName   : st7567_font_write_utf8_str()
* Description    : Lcm写UTF8字符串
* EntryParameter : *addr,字符串写入起始地址, str,要写的字符串
* ReturnValue    : 0,成功, -1,失败
**************************************************************************************/
int8_t st7567_font_write_utf8_str(struct st7567_addr_s *addr, uint8_t *str)
{
    uint16_t i = 0, cnt = 0, offset = 0;
    const uint8_t *font_dot = NULL;
    struct st7567_dot_s dot;
    static uint8_t dot_data[2][512];

    if(NULL == addr || NULL == str){
        return -1;
    }

    memset(&dot_data, 0, sizeof(dot_data));
    memset(&dot, 0, sizeof(dot));
    dot.addr.page = addr->page;
    dot.addr.column = addr->column;
    dot.high = ST7567_FONT_HIGH;

    while(0 != str[i]){
        font_dot = st7567_font_utf8_get_dot(&str[i]);
        if(NULL == font_dot){
            return -1;
        }
        if(str[i] < 0xA1){
            memcpy(&dot_data[0][cnt], font_dot, \
                ST7567_FONT_WIDE*ST7567_FONT_HIGH/2/8/2);
            offset = ST7567_FONT_WIDE*ST7567_FONT_HIGH/2/8/2;
            memcpy(&dot_data[1][cnt], \
                font_dot + offset, ST7567_FONT_WIDE*ST7567_FONT_HIGH/2/8/2);
            dot.wide += ST7567_FONT_WIDE/2;
            dot.count += ST7567_FONT_WIDE*ST7567_FONT_HIGH/2/8;
            i += 1;
        }else{
            memcpy(&dot_data[0][cnt], font_dot, \
                ST7567_FONT_WIDE*ST7567_FONT_HIGH/8/2);
            offset = ST7567_FONT_WIDE*ST7567_FONT_HIGH/8/2;
            memcpy(&dot_data[1][cnt], \
                font_dot + offset, ST7567_FONT_WIDE*ST7567_FONT_HIGH/8/2);
            dot.wide += ST7567_FONT_WIDE;
            dot.count += ST7567_FONT_WIDE*ST7567_FONT_HIGH/8;
            i += 3;
        }
        cnt += offset;
        if((dot.addr.column + dot.wide) >= ST7567_WIDE_MAX){
            break;
        }
    }

    memcpy(dot.data, &dot_data[0][0], cnt);
    memcpy(dot.data + cnt, &dot_data[1][0], cnt);
    st7567_write_dot(&dot);

    return 0;
}


